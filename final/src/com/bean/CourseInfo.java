package com.bean;

public class CourseInfo {
	private String courseID;
	private String courseName;
	private String classID;
	private String teacherName;
	private String title;
	private int stuNum;
	private int mode;
	private int startWeek;
	private int endWeek;
	private int theoryHours;
	private int experimentHours;
	private double credit;
	public double getCredit() {
		return credit;
	}
	public void setCredit(double credit) {
		this.credit = credit;
	}
	public String getCourseID() {
		return courseID;
	}
	public void setCourseID(String courseID) {
		this.courseID = courseID;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getClassID() {
		return classID;
	}
	public void setClassID(String classID) {
		this.classID = classID;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getStuNum() {
		return stuNum;
	}
	public void setStuNum(int stuNum) {
		this.stuNum = stuNum;
	}
	public int getMode() {
		return mode;
	}
	public void setMode(int mode) {
		this.mode = mode;
	}
	public int getStartWeek() {
		return startWeek;
	}
	public void setStartWeek(int startWeek) {
		this.startWeek = startWeek;
	}
	public int getEndWeek() {
		return endWeek;
	}
	public void setEndWeek(int endWeek) {
		this.endWeek = endWeek;
	}
	public int getTheoryHours() {
		return theoryHours;
	}
	public void setTheoryHours(int theoryHours) {
		this.theoryHours = theoryHours;
	}
	public int getExperimentHours() {
		return experimentHours;
	}
	public void setExperimentHours(int experimentHours) {
		this.experimentHours = experimentHours;
	}
	
	
}
